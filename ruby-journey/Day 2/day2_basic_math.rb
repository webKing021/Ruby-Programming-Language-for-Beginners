puts 4 * 3 # Output: 12
puts 12 / 3 # Output: 4
puts 17 % 5 # Output: 2 (modulo = remainder)
puts 2 ** 3 # Output: 8 (exponentiation)

puts (5 + 3) * 2 # Output: 16 (parentheses change precedence)
puts 10 - (2 + 3) # Output: 5 (parentheses change precedence)
puts 8 / 2 + 3 # Output: 7 (division before addition)